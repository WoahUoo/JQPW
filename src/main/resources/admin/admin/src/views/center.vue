      ref="ruleForm"
      :model="ruleForm"
      label-width="200px"
    >  
     <el-row>
        <el-form-item :style='{"margin":"0 0 20px 0"}'   v-if="flag=='shoupiaoyuan'"  label="售票员账号" prop="shoupiaoyuanzhanghao">
          <el-input v-model="ruleForm.shoupiaoyuanzhanghao" readonly              placeholder="售票员账号" clearable></el-input>
        </el-form-item>
        <el-form-item :style='{"margin":"0 0 20px 0"}'   v-if="flag=='shoupiaoyuan'"  label="姓名" prop="xingming">
          <el-input v-model="ruleForm.xingming"               placeholder="姓名" clearable></el-input>
        </el-form-item>
