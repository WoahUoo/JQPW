				var obj = this.$storage.getObj('crossObj');
				for (var o in obj){
						if(o=='menpiaomingcheng'){
							this.ruleForm.menpiaomingcheng = obj[o];
							this.ro.menpiaomingcheng = true;
							continue;
						}
